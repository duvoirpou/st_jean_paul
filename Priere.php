                                <h3>La prière des « cinq doigts » du pape François</h3>
                                <p>
                                1. Le pouce est le doigt le plus proche de vous. Donc commencez par prier pour ceux qui vous sont les plus proches. Ce sont les plus susceptibles de revenir à vos mémoires. Prier pour les gens qui nous sont très chers est un doux devoir. Restons quelques instants en silence pour penser aux membres de nos familles, nos amis, nos proches, nos collègues et les personnes connues ou inconnues que nous avons croisées en ce jour.
                                </p>
                                <p>
                                2. Ensuite, l’index. Priez pour ceux qui enseignent, ceux qui s’occupent de l’éducation et des soins médicaux : pour les enseignants, les professeurs, les médecins et les prêtres, les catéchistes. Ils ont besoin de soutien et de sagesse afin qu’ils puissent montrer le droit chemin aux autres. Ne les oubliez pas dans vos prières. Arrêtons-nous un moment pour méditer sur l’importance de l’enseignement de la petite enfance jusqu’à l’âge adulte, et sur l’intérêt de soins de santé de qualité prodigués respectivement par un corps professoral et médical motivés.
                                </p>
                                <p>
                                3. Le doigt qui suit est le majeur, le plus long. Il nous rappelle nos gouvernants. Priez pour le Président, pour les députés, pour les entrepreneurs et pour les administrateurs. Ce sont eux qui dirigent le destin de notre pays et sont chargés de guider l’opinion publique. Ils ont besoin de l’aide de Dieu. Attardons-nous un peu sur tous les types de dirigeants, à tous les niveaux que ce soit, la politique, avec les pouvoirs législatif et exécutif, la magistrature avec le pouvoir judiciaire, l’économie avec les chefs d’entreprise, la presse, les dignitaires spirituels.
                                </p>
                                <p>
                                4. Le quatrième doigt est l’annulaire. Bien que cela puisse surprendre la plupart des gens, c’est notre doigt le plus faible, et tout professeur de piano peut le confirmer. Vous devez vous rappeler de prier pour les faibles, pour ceux qui ont beaucoup de problèmes à résoudre ou qui sont éprouvés par la maladie. Ils ont besoin de vos prières jour et nuit. Il n’y aura jamais trop de prières pour ces personnes. Nous sommes aussi invités à prier pour les mariages. A présent, prions pour les personnes en position de faiblesse : les malades, les personnes handicapées, les personnes mutilées, abandonnées, rejetées, exclues, humiliées, endeuillées, les marginaux, les victimes en tout genre, les sinistrés, les accidentés. Ceux et celles qui doivent se reconstruire après un traumatisme. Etant donné que l’annulaire est le doigt de l’alliance, demandons également au Seigneur de protéger les couples unis par le mariage.
                                </p>
                                <p>
                                5. Et enfin, il y a notre auriculaire, le plus petit de tous les doigts, aussi petit que nous devons nous tenir devant Dieu et devant les autres. Comme le dit la Bible, les derniers seront les premiers. Le petit doigt est là pour vous rappeler que vous devez prier pour vous-même. Ce n’est que lorsque vous avez prié pour les quatre autres groupes, que vous pouvez le faire pour vous en toute confiance. Enfin, même si Dieu votre Père sait bien ce qu’il vous faut, avant que vous ne le lui demandiez (Mt 6,8), vous pouvez maintenant lui demander dans votre for intérieur de subvenir à vos besoins spirituels et matériels, et si c’est conforme à sa volonté et à son plan d’amour sur vous, il vous l’accordera dans son infinie miséricorde.
                                </p>
