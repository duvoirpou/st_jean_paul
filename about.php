                        <h3>Historique</h3>
                        <h2>A propos de l'école Jean Paul II</h2>
                        <p>
                            L’école de Gastronomie Jean Paul II en sigle (EGJPII) lancé à Casablanca au Maroc elle a vu le jour le 09 Octobre 2017 à Brazzaville, Inaugurer par le Nonce APOSTOLIQUE Excalate MOLINA Représentant du Pape Au CONGO. Sous l’initiative de la direction du collège des cuisiniers. Située au cœur du quartier Bacongo, plus précisément dans la rue Montaigne  à Brazzaville.
                            <br>
                            Elle est Dirigé par Monsieur H. Boris SONGUESSA technicien en arts culinaires ayant travaillé dans plusieurs hôtels et restaurants de la place au Congo, sorti de l’école de tourisme et hôtellerie (CONGO) et de l’école de   la Toque Blanche  et D’ALIMA  du MAROC.
                            <br>
                            Ce dernier a pensé transmettre son savoir faire aux jeunes congolais, qui veulent faire de ce métier un métier d’avenir qui n’a plus de frontière.
                        </p>
                        <p>
                            N’ayant plus de frontière ladite école à mis à la disposition des étudiants plusieurs rubriques pour leur donne les bases des métiers de la gastronomie. La formation à l’EGJPII s’effectue en deux étapes à savoir : une formation théorique avec un programme muscle selon les normes internationales et une formation pratique faite sur place sous l’œil attentif et bienveillant des chefs cuisiniers exerçant dans des entreprises de la place.
                            <br>
                            Sa mission consiste aussi à leur offrir la possibilité d’exceller, grâce à des formations de pointe et un savoir transmis avec passion et générosité par des formateurs expérimentés.

                        </p>
