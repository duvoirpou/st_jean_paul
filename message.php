                                <h3>Bienvenue à Saint Jean Paul II</h3>
                                <p>
                                    Arriver au lycée hôtelier du Touquet nous demande d’arriver discrètement par Merlimont ou Stella en traversant le golf de la mer, ou plus souvent de longer les quais du joli port d’Étaples, franchir le pont rose, et découvrir une station balnéaire construite dans la forêt, et blottie contre le rivage de la manche à deux pas de l’estuaire de la canche. On emprunte l’avenue Général de Gaulle qui nous conduit au Rond-point des sports, en amont duquel trône fièrement dressée vers le ciel, la tour d’internat du lycée dessinée par Pierre-André Dufétel.
                                                <br>
                                    Au Touquet, ville des quatre saisons, des sports, des arts, et de la culture, les restaurants et hôtels de prestige sont nombreux. Il en est d’ailleurs de même dans l’ensemble du montreuillois.
                                                <br>
                                    Entrer au lycée hôtelier du Touquet, c’est s’engager dans une démarche singulière, qui associe rigueur, exigence, excellence, esthétique, savoir-vivre, pour acquérir, pratiquer, développer, et transmettre des savoirs et valeurs reconnus dans le monde entier.
                                                <br>
                                    Il y a dans la générosité des métiers que notre établissement continue d’influencer, des principes forts qui prennent leurs racines dans l’éducation à la vie en groupe, mais aussi dans les exigences que l’on a pour soi. Les anciens élèves de notre Lycée hôtelier croisés à travers le monde sont toujours très reconnaissants envers le lycée hôtelier, notamment dans ce qu’il a apporté dans leur vie : savoir-faire, savoir-vivre, et savoir-être. Ils insistent souvent sur ce dernier point qui leur a permis de construire une vie d’adulte responsable.
                                                <br>
                                    L’amour d’accueillir, de servir, de nourrir, et de régaler, est pour nous une deuxième nature, car il y a derrière cela, la garantie d’un bon moment partagé, la joie d’un savoir-faire qui appelle le dépassement de soi, pour une ambition qui échappe à l’intérêt personnel, et qui confine au bonheur pour tous.
                                                <br>
                                    Si l’on acquiert au lycée hôtelier une capacité de travail importante et solide, c’est parce que l’on a appris de ceux qui nous ont précédés, que la discipline, l’obéissance, le respect, et l’admiration, dus aux adultes croisés en ce lieu, sont les vertus qui permettent un jour à chacun, fort de cette solide base éducative liée à l’excellence professionnelle, de réaliser son rêve. C’est ce compagnonnage adulte-élève, particulièrement présent chez nous, qui crée les conditions d’un parcours riche pour chacun, ne distinguant plus la vie, du travail, mais une vie au travail qui souvent conduit à bâtir ce que l’on regardera au bout du chemin comme le travail d‘une vie.
                                                <br>
                                    C’est donc sa place dans la société que l’on vient construire au lycée hôtelier, une place de choix dans un pays reconnu pour les métiers auxquels nous formons. On dit qu’ils sont des fleurons de la culture française et de notre économie. C’est pourquoi nos élèves trouvent facilement des terrains de stage à l’étranger, et parfois s’y installent ; ils contribuent ainsi également au rayonnement du lycée dans le monde.
                                                <br>
                                    Ici les élèves travaillent, obéissent, respectent et admirent leurs professeurs, dans un climat scolaire bienveillant, compréhensif, et exigeant. Ils dessinent chez nous déjà solidement leur parcours professionnel et personnel. Cette culture de la discipline individuelle et collective donne la force, à nos lycéens et à nos étudiants, de construire leur libre arbitre de jeune adulte, qui permet à l’audace de surgir, et à la sérieuse réputation de professionnels reconnus de s’installer pour durer.
                                                <br>
                                    Soyons heureux et fiers de vivre au quotidien au lycée hôtelier du Touquet !
                                </p>
                                
